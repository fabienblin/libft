#include "ft_printf.h"

int	ft_checkstr(t_format *f)
{
	while (f)
	{
		while (*f->tostring)
		{
			//55296 a 57343
			//1101 1000 0000 0000 a 1101 1111 1111 1111
			//1011 1000 1011 01100 1000 0000 a 1011 1000 10
			if (*(wchar_t *)f->arg >= 0xd800 && *(wchar_t *)f->arg <= 0xdfff)
				return (0);
			f->tostring++;
		}
		f = f->next;
	}
	return(1);
}